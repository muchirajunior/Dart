/**
 * constants in dart
 * using const
 * usnig final keyword
 */

void main() {
  
  //final keyword
  //final keyword is initialized on use
  final name="MUCHIRA JUNIOR";
  final String Place="Nyeri"; //adding a variable type is optional

  //const keyword
  //const keyword is initialized on compile time
  const PI=3.14;
  const double Gravity=9.8;

  print('name is $name place  is $Place \n pi is $PI and gravitional force is $Gravity');

}